/*  Operadores em JS */
// permitem realizar operações mat de comparações e lógicas

/** Aritméticos */
console.log(2+2);
console.log(2-2);
console.log(2*3);
console.log(8/2);

/** Comparadores */
console.log(2 == 2);
console.log(3 != 3);

//igual estrito => true = se valor e tipo são iguais/ Valor iguais, tipos diferentes = false
console.log(2 === '2')
console.log(2 === 2);

// não igual estruto !==, mesma logica do igual comparando tipo e valor
console.log(3 !== "3");

console.log(2 >= 3);
console.log(2 <= 3);

